# Operadores de Comparação: Usados para comparar valores e retornam True ou False

# Operador == - Significado: Igual - Exemplo:
print('Verificando a comparação de numeros iguais: ', 10 == 10)

# Operador == - Significado: Igual - Exemplo:
print('Verificando a comparação de numeros diferentes: ', 10 == 11)

# Operador == - Significado: Igual - Exemplo:
print('Verificando a comparação de numero e string: ', 10 == '10')

# Operador != - Significado: Diferente - Exemplo:
print('Verificando diferença na comparação de numero e string: ', 10 != '10')

# Operador > - Significado: Maior que - Exemplo:
print('Verificando o maior valor na comparação de numeros iguais: ', 10 > 10)

# Operador > - Significado: Maior que - Exemplo:
print('Verificando o maior valor na comparação de numeros diferentes: ', 11 > 10)

# Operador < - Significado: Menor que - Exemplo:
print('Verificando o menor valor na comparação de numeros diferentes: ', 11 < 10)

# Operador < - Significado: Menor que - Exemplo:
print('Verificando o menor valor na comparação de numeros diferentes: ', 10 < 11)

# Operador >= - Significado: Maior ou igual - Exemplo:
print('Verificando se o valor é maior ou igual na comparação de numeros diferentes: ', 11 >= 12)

# Operador >= - Significado: Maior ou igual - Exemplo:
print('Verificando se o valor é maior ou igual na comparação de numeros iguais: ', 12 >= 12)

# Operador >= - Significado: Maior ou igual - Exemplo:
print('Verificando se o valor é maior ou igual na comparação de numeros diferentes: ', 13 >= 12)

# Operador <= - Significado: Menor ou igual - Exemplo:
print('Verificando se o valor é menor ou igual na comparação de numeros diferentes: ', 13 <= 12)

# Operador <= - Significado: Menor ou igual - Exemplo:
print('Verificando se o valor é menor ou igual na comparação de numeros iguais: ', 12 <= 12)

# Operador <= - Significado: Menor ou igual - Exemplo:
print('Verificando se o valor é menor ou igual na comparação de numeros iguais: ', 11 <= 12)